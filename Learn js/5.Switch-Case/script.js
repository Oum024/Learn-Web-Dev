const grade = 'A';

switch(grade)
{
    case 'A':
        console.log('A -> Very Good');
        break;

    case 'B':
        console.log('B -> Good');
        break;

    case 'C':
        console.log('C -> Need Improvement');
        break;

    case 'D' : default:
        console.log('Fail')
}